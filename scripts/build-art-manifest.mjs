#!/usr/bin/env node
// Build the content-art pregeneration manifest from the three prompt sheets
// (docs/content-art-{worlds,species,technology}.md). Emits a paste-ready
// checklist: one entry per subject with the full assembled prompt (STYLE
// BLOCK + ISOLATION + FRAMING + SUBJECT, markdown stripped) plus both output
// crops (1:1 + 16:9) and their file paths.
//
// The docs are the source of truth; this file is a derived projection. Tweak a
// prompt in a doc, then re-run:
//   npm run art:manifest      (or: node scripts/build-art-manifest.mjs)

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");

const DOCS = [
  { axis: "worlds", dir: "worlds", file: "docs/content-art-worlds.md" },
  { axis: "species", dir: "species", file: "docs/content-art-species.md" },
  { axis: "technology", dir: "tech", file: "docs/content-art-technology.md" },
];

const RATIOS = [
  { ratio: "sq", ar: "1:1" },
  { ratio: "wide", ar: "16:9" },
];

const MID_DOT = "·"; // ·
const ARROW = "→"; //   →

/** Strip markdown emphasis/backticks and collapse whitespace to clean prose. */
function clean(s) {
  return s
    .replace(/\*\*/g, "")
    .replace(/`/g, "")
    .replace(/\*/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

/**
 * The blockquote paragraphs immediately following a `## HEADING`. Skips any
 * intro prose between the heading and the first `>` line; a bare `>` splits
 * paragraphs; the first non-`>` line after the quote ends it.
 */
function blockquoteParagraphs(text, headingRe) {
  const lines = text.split("\n");
  let i = lines.findIndex((l) => headingRe.test(l));
  if (i === -1) return [];
  const paras = [];
  let cur = [];
  let started = false;
  for (i++; i < lines.length; i++) {
    const line = lines[i];
    if (line.startsWith(">")) {
      started = true;
      const content = line.replace(/^>\s?/, "").trim();
      if (content === "") {
        if (cur.length) {
          paras.push(cur.join(" "));
          cur = [];
        }
      } else {
        cur.push(content);
      }
    } else if (started || line.startsWith("#")) {
      break; // quote ended, or a new heading arrived before any quote
    }
  }
  if (cur.length) paras.push(cur.join(" "));
  return paras;
}

/** Every `#### id · name  → `slug`` plate heading + its subject blockquote. */
function parsePlates(text) {
  const lines = text.split("\n");
  const start = lines.findIndex((l) => /^## Subject prompts\b/.test(l));
  const headingRe = new RegExp(
    `^####\\s+(\\S+)\\s+${MID_DOT}\\s+(.+?)\\s+${ARROW}\\s+\`([^\`]+)\``,
  );
  const plates = [];
  let cur = null;
  for (let i = start === -1 ? 0 : start + 1; i < lines.length; i++) {
    const m = lines[i].match(headingRe);
    if (m) {
      if (cur) plates.push(cur);
      cur = { id: m[1], name: m[2].trim(), subj: [] };
    } else if (cur && lines[i].startsWith(">")) {
      const content = lines[i].replace(/^>\s?/, "").trim();
      if (content) cur.subj.push(content);
    }
  }
  if (cur) plates.push(cur);
  return plates.map((p) => ({ id: p.id, name: p.name, subject: clean(p.subj.join(" ")) }));
}

let anchor = null;
const shared = { styleBlock: null, byAxis: {} };
const plates = [];

for (const { axis, dir, file } of DOCS) {
  const text = readFileSync(join(ROOT, file), "utf8");

  const styleBlock = clean(blockquoteParagraphs(text, /^## STYLE BLOCK\b/)[0] ?? "");
  const isolation = clean(blockquoteParagraphs(text, /^## ISOLATION\b/)[0] ?? "");
  const framing = clean(blockquoteParagraphs(text, /^## FRAMING\b/)[0] ?? "");
  if (shared.styleBlock === null) shared.styleBlock = styleBlock;
  shared.byAxis[axis] = { isolation, framing };

  if (anchor === null) {
    const anchorPara =
      blockquoteParagraphs(text, /^## STYLE ANCHOR\b/).find((p) => p.includes("STYLE ANCHOR PROMPT")) ?? "";
    anchor = {
      note: "Generate this once and reuse across every plate as a style reference (Midjourney --sref, or the equivalent). One anchor for the whole library, not one per axis. If it over-composes a single-subject render, lower the style weight (--sw 50-80).",
      prompt: clean(anchorPara).replace(/^STYLE ANCHOR PROMPT\s*[—–-]\s*/, ""),
    };
  }

  for (const p of parsePlates(text)) {
    plates.push({
      id: p.id,
      axis,
      name: p.name,
      prompt: clean([styleBlock, isolation, framing, p.subject].join(" ")),
      outputs: RATIOS.map(({ ratio, ar }) => ({
        ratio,
        ar,
        path: `${dir}/${ratio}/${p.id}.webp`,
      })),
    });
  }
}

const manifest = {
  $comment:
    "Generated by scripts/build-art-manifest.mjs from docs/content-art-{worlds,species,technology}.md. Do not hand-edit — re-run `npm run art:manifest`.",
  style: "cinematic matte painting",
  ratios: Object.fromEntries(RATIOS.map(({ ratio, ar }) => [ratio, ar])),
  usage:
    "For each plate, produce BOTH outputs. Paste `prompt`, append your tool's aspect flag for that output (output.ar, e.g. --ar 16:9) and your style reference (--sref <anchor>, --sw 50-80 if it over-composes), and save the render to output.path. The STYLE BLOCK / ISOLATION / FRAMING are already baked into `prompt`; `shared` and `anchor` are for reference.",
  anchor,
  shared,
  counts: { plates: plates.length, images: plates.length * RATIOS.length },
  plates,
};

const out = join(ROOT, "docs/content-art-manifest.json");
writeFileSync(out, JSON.stringify(manifest, null, 2) + "\n");
console.log(
  `wrote ${out}\n  ${manifest.counts.plates} plates -> ${manifest.counts.images} images` +
    ` (${plates.filter((p) => p.axis === "worlds").length} worlds, ` +
    `${plates.filter((p) => p.axis === "species").length} species, ` +
    `${plates.filter((p) => p.axis === "technology").length} technology)`,
);
